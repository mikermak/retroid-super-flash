// Blit the QR at (x,y); uses fixed B/W palette indices 236/237 so it scans on any theme.
static void render_qr_retroid(volatile uint8_t *frame, unsigned x, unsigned y) {
  MEM_PALETTE[236] = RGB2GBA(0xffffff);
  MEM_PALETTE[237] = RGB2GBA(0x000000);
  for (unsigned r = 0; r < QR_SPAN; r++)
    dma_memset16(&frame[(y + r) * SCREEN_WIDTH + x], dup8(236), QR_SPAN/2);
  for (unsigned my = 0; my < QR_N; my++)
    for (unsigned mx = 0; mx < QR_N; mx++)
      if (qr_retroid[my*QR_N + mx]) {
        unsigned px = x + (mx + QR_QUIET)*QR_MOD, py = y + (my + QR_QUIET)*QR_MOD;
        *(volatile uint16_t*)&frame[py*SCREEN_WIDTH + px]       = dup8(237);
        *(volatile uint16_t*)&frame[(py+1)*SCREEN_WIDTH + px]   = dup8(237);
      }
}
